  <h2>Getting Started With Scryptic</h2>

  <div>
    <h3>Step One: Upload it!</h3>
    <p class="screenshot"><img src="/images/faq-howto1.jpg" alt="Scryptic - Step 1" /></p>
    <p>Scryptic allows you to upload multiple PDF documents for watermarking. Simply click "Choose File" to locate your PDF document, then when you're ready click "Upload"!</p>
  </div>

  <div>
    <h3>Step Two: Mark it!</h3>
    <p class="screenshot"><img src="/images/faq-howto2.jpg" alt="Scryptic - Step 2" /></p>
    <p>Creating a watermark has never been easier!  Just type the text you want to use into the "Watermarks" field and click "Create".</p>
    <p>Scryptic also lets you set the layout of your watermarks!  Click the "Config" tab to choose any of Scyptic's options.  You can set the watermark layout (Top &amp; Bottom, Left &amp; Right, or Diagonal).  You can also choose different fonts, sizes, and even color and transparency!  Preview it with the handy "Preview" button, and when you're happy, save it!</p>
    <p>Scryptic lets you save multiple watermarks to your account to reuse over and over!</p>
  </div>

  <div>
    <h3>Step Three: Print it!</h3>
    <p class="screenshot"><img src="/images/faq-howto3.jpg" alt="Scryptic - Step 3" /></p>
    <p>Use Scryptic's handy Preview feature to see how your document will look, then when you're ready, click "Print"!</p>
  </div>

